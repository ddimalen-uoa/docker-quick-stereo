<h2><a href="http://www.conftool.net/en/features.html">Features</a></h2>
ConfTool is a robust and mature system, that supports many tasks of the conference organizer:
<ul>
<li> online submission of papers,
<li> assignment to reviewers based on matching conference topics,
<li> download of the assigned papers by the reviewers,
<li> submission of reviews,
<li> support of the local PC-meeting,
<li> bulk mailings to reviewers and authors,
<li> conference participant registration with a flexible registration form generator,
<li> registration of participants at the conference site,
<li> and <a href="http://www.conftool.net/en/features.html">much more...</a>
</ul>
